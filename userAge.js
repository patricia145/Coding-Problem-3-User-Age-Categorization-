let age = 19;

if (age < 5) {
    console.log("You are a Toddler.");  
} else if (age <= 12) {
    console.log("You are a Child.");
} else if (age <= 19) {
    console.log("You are a Teenager.");
} else if (age <= 35) {
    console.log("You are a Adult.");
} else if (age <= 60) {
    console.log("You are a Middle-age.");
} else {
    console.log("You are a Senior.")
}